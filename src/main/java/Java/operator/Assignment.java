package Java.operator;

public class Assignment {
    public static void main(String[] args) {
        //+=.-=,*=,=
        int alpha=10;
        int beta=alpha;
        System.out.println(beta);
        alpha+=15;
        System.out.println("Alpha += value"+alpha);
        beta-=5;
        System.out.println("Beta -= value"+beta);
    }
}
